    private void txtBorrowedIDKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBorrowedIDKeyReleased
        try {
            String sql = "SELECT MASOSACH , MADG , NGAYTRA FROM PHIEUMUON WHERE MAPHIEU = '"+txtBorrowedID.getText()+"' and TRANGTHAI = '0'";
            
            pst = (PreparedStatement) conn.prepareStatement(sql);
            rs = pst.executeQuery();
            
            if (rs.next())
            {
                txtBookID.setText(rs.getString("MASOSACH"));
                txtMemID.setText(rs.getString("MADG"));
                String r_date = rs.getString("NGAYTRA");
                LocalDate today = LocalDate.now();
                LocalDate rday = LocalDate.parse(r_date);
                
                Long day_gap = ChronoUnit.DAYS.between(rday, today);
                
                if (day_gap > 0)
                {
                txtLateday.setText(day_gap.toString());
                
                Long fine = 5000*day_gap;
                txtFine.setText(String.valueOf(fine));
                }
                else
                {
                    txtLateday.setText("0");
                    txtFine.setText("0");
                }
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_txtBorrowedIDKeyReleased

