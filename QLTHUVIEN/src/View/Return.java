    private void autoId(){
        try {
             String sql="SELECT MAPHIEUTRA FROM dbo.PHIEUTRA ORDER BY MAPHIEUTRA DESC";
         
            pst=(PreparedStatement) conn.prepareStatement(sql);
            rs=pst.executeQuery();
           if(rs.next())
           {
                String rnno=rs.getString("MAPHIEUTRA");
                int co=rnno.length();
               String txt= rnno.substring(0, 2);
               String num=rnno.substring(2, co);
               int n=Integer.parseInt(num);
               n++;
               String snum=Integer.toString(n);
               String ftxt=txt+snum;
               txtReturnBID.setText(ftxt);
            
           }
           else
           {
               txtReturnBID.setText("PT1000");
           }
           
                            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
       
    }


